// Helper function to get neighborhoods by city
export function getNeighborhoodsByCity(citySlug: string): NeighborhoodData[] {
  return neighborhoods.filter(n => n.citySlug === citySlug);
}

// Helper function to get a single neighborhood
export function getNeighborhood(neighborhoodSlug: string, citySlug: string): NeighborhoodData | undefined {
  return neighborhoods.find(n => n.slug === neighborhoodSlug && n.citySlug === citySlug);
}

// Get all unique cities that have neighborhoods
export function getCitiesWithNeighborhoods(): string[] {
  return [...new Set(neighborhoods.map(n => n.citySlug))];
}
